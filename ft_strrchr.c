#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*res;

	res = NULL;
	while (*s != '\0')
	{
		if (*s == c)
			res = (char *)s;
		s++;
	}
	if (*s == c)
		return ((char *)s);
	return (res);
}